const u = @import("../utils.zig");
const SHA256 = @import("SHA-256.zig").Sha256;
const SHA512 = @import("SHA-256.zig").Sha512;

pub const SHA224 = struct {
    pub fn hash(data: []const u8) [28]u8 {
        var s = SHA256.init224();
        s.update(data);
        const full = s.final();
        var out: [28]u8 = undefined;
        u.copyBytes(&out, full[0..28]);
        return out;
    }
};

pub const SHA384 = struct {
    pub fn hash(data: []const u8) [48]u8 {
        var s = SHA512.init384();
        s.update(data);
        const full = s.final();
        var out: [48]u8 = undefined;
        u.copyBytes(&out, full[0..48]);
        return out;
    }
};

test "SHA-224 empty" {
    const h = SHA224.hash("");
    const expected = [28]u8{
        0xd1, 0x4a, 0x02, 0x8c, 0x2a, 0x3a, 0x2b, 0xc9, 0x47, 0x61,
        0x02, 0xbb, 0x28, 0x82, 0x34, 0xc4, 0x15, 0xa2, 0xb0, 0x1f,
        0x82, 0x8e, 0x6a, 0xd0, 0x1c, 0x5c, 0x6a, 0x8f,
    };
    for (h, expected) |a, b| { if (a != b) return error.TestUnexpectedResult; }
}

